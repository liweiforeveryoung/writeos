#include "global.h"

short min_short(short lhs, short rhs) {
    if (lhs < rhs) {
        return lhs;
    } else {
        return rhs;
    }
}

short max_short(short lhs, short rhs) {
    if (lhs > rhs) {
        return lhs;
    } else {
        return rhs;
    }
}

